# Lesson 3: Prompt for Favorite Color

puts "What is your favorite color?"
color = gets.chop
#puts "Wow! My favorite color is #{color} also!"

# Lesson 4: Using an If Statement

if color == "blue"
	puts "Wow! My favorite color is #{color} also!"
elsif color == "yellow"
	puts "#{color} is not as bad as green, but I can dig it."
elsif color == "black"
	puts "Henry loves #{color}, still I am a bigger fan of blue."
	
else
	puts "I'm not a big fan of #{color}."
end

